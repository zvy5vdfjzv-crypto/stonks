function generatePriceHistory(basePrice, points = 48) {
  const history = []
  let price = basePrice * (0.7 + Math.random() * 0.3)
  for (let i = 0; i < points; i++) {
    const change = (Math.random() - 0.48) * basePrice * 0.06
    price = Math.max(price + change, basePrice * 0.1)
    history.push({ time: i, price: parseFloat(price.toFixed(2)) })
  }
  history[history.length - 1].price = basePrice
  return history
}

